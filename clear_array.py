def clear_array(lst: list):
    """Takes a given list and clears out all of the elements within the list.

    Examples:
        >>> mylst\n
        [('a', 1, 'John'), ('b', 2, 'Alice'), ('c', 3, 'Bob')]
        >>> clear_array(mylst)\n
        >>> mylst\n
        []
    """
    return lst.clear()

